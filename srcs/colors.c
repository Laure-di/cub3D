#include "../inc/cub3D.h"

int	create_rgb(int r, int g, int b)
{
	return (r << 16 | g << 8 | b);
}

int	create_rgbt(int r, int g, int b, int t)
{
	return (t << 24 | r << 16 | g << 16 | b);
}
